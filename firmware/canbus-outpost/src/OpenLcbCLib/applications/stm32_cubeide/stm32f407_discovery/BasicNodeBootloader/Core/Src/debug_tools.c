void PrintInt64(uint64_t n)
{

    printf("0x%04X", (uint16_t)((n >> 48) & 0xFFFF));
    printf("%04X", (uint16_t)((n >> 32) & 0xFFFF));
    printf("%04X", (uint16_t)((n >> 16) & 0xFFFF));
    printf("%04X\n", (uint16_t)((n >> 0) & 0xFFFF));
}

